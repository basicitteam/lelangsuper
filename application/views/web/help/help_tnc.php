<h4>D. Konfirmasi &amp; Pembayaran Menang Lelang</h4>

<ol>
<li>Pemenang Lelang akan dikirimkan notifikasi via SMS atau via email atau via telepon, dan Pemenang harus mengkonfirmasikannya kembali ke pihak LELANGSUPER.COM.</li>
<li>Pemenang berhak membayar barang menang lelang sesuai harga terakhir yang ditawar oleh Pemenang dengan ditambahkan ongkos kirim barang.</li>
<li>Pembayaran atas barang yang dimenangkan harus dilakukan max. 7 hari kerja setelah lelang berakhir.</li>
</ol>

<h4>E. Pembatalan Menang Lelang</h4>
<ol>
<li>Pemenang yang tidak mengkonfirmasi atau melakukan pembayaran dalam kurun waktu 7 hari kerja, maka kemenangan akan dibatalkan oleh pihak Admin LELANGSUPER.COM.</li>
<li>Kemenangan dapat dibatalkan jika diketahui bahwa Pemenang melakukan kecurangan atau melanggar Peraturan dan Tata Tertib.</li>
<li>Kemenangan dapat dibatalkan jika peserta memenangkan lelang tetapi berada dalam kondisi pernah menang lelang pada periode waktu 7 (tujuh) hari sebelumnya. Contohnya username A menang lelang pada hari Senin, maka username A boleh menang lelang lagi pada hari Selasa minggu depan. Aturan ini tidak berlaku untuk Jenis lelang BEBAS.</li>
<li>Ketidaksesuaian kartu identitas dengan data diri Pemenang akan mengakibatkan batalnya kemenangan.</li>
<li>Pemenang Lelang dapat membatalkan barang menang lelang yang telah diterima jika ada ketidaksesuaian, kerusakan atau disfungsi dari barang tersebut. Pemenang dapat mengembalikannya ke pihak LELANGSUPER.COM, dan dana barang menang lelang (diluar ongkir) yang telah disetor Pemenang akan dikembalikan. Garansi ini berlangsung max 7 hari
kerja setelah Pemenang menerima barang tersebut.</li>
</ol>


