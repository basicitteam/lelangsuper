                            <h5>How do I find things to bid on?</h5>

                            <ul>

                              <li>Browse our catalog (either in printed form or <a href="pdfcurrent.cfm">online</a>)</li>

                              <li>Select a category which interests you from the list on the left navigation menu.</li>

                              <li>Use one of the several search tools we've provided for you 
                                (by 
                                <a href="#">item number</a> or name, 
                                in 
                                <a href="#">gallery format</a>, 
                                <a href="#">past auctions</a>, 
                                or 
                                <a href="advancedsearch.cfm">advanced search</a>).
                              </li>

                            </ul>

                            <p>Note: The search tools are found on the left navigation menu as well as our top menu bar.</p>



                            <a name="add1"></a>

                            <p>

                            </p><h5>How do I place a bid online?</h5>

                            Bidding online is easy!  

                            <ul>

                              <li>Navigate to the item's detailed description/bid page.</li>

                              <li>Click on the field labeled <i>New Bid</i>; the next available bid amount is automatically filled in for you.</li>

                              <li>(optionally) Select a <i>Max Bid</i> amount from the list provided.</li>

                              <li>Click the <img src="images/btn_SubmitBid_on.gif" width="72" height="23" align="absmiddle" border="0"> button.</li>

                              <li>You are asked to verify your bid and max bid amounts; click the [OK] button to confirm your bid, or the [Cancel] button to make changes.</li>

                            </ul>

                            <p></p>

                            <p>

                              Note: If this is your first bid in this month's auction, and you did not click the <img src="images/btn_ConfirmParticipation_on.gif" width="102" height="23" align="absmiddle" border="0"> button when you first logged in, you will be asked to do so now.

                            </p>

                            <p>

                              If your bid is successful (you are the new high bidder), you will be brought to your BidTracker list. 

                              If you are outbid (by another bidder's max bid amount), you will be notified on screen and remain on the item's detailed description/bid page.

                            </p>



                            <a name="add2"></a>

                            <p>

                            </p><h5>Can I bid more than the default (<i>Next Bid</i>) amount?</h5>

                            You can raise the <i>New Bid</i> amount which is automatically filled in for you, by entering a higher amount. However we recommend that you instead select a <i>Max Bid</i> amount for this purpose.

                            <p></p>

                            <p>

                            <a name="remove1"></a>

                            </p><h5>Can I raise my maximum bid amount (or add a maximum bid amount)?</h5>

                            If you are the current high bidder on an item, you can either add or raise a max bid amount, by selecting an amount from the <i>Max Bid</i> list found on the item's detailed description page and clicking the [Submit Bid] button.

                            <p></p>

                            <p>Note: Leave the field labeled <i>New Bid</i> blank in order to avoid raising your actual bid amount.</p>



                            <p>

                            <a name="remember"></a>

                            </p><h5>What do I do if I can't remember my password or bidder number?</h5>

                            If you have forgotten your password, <a href="email_password.cfm">click here</a> and follow the instructions provided to receive it via email.

                            <p></p>

                            <p>

                            If you have forgotten your bidder number, just give us a call at (800) 937-3880. We can verify your information and even register you to bid if you have a bidder number on file with us.

                            </p>



